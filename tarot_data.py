def get_tarot_interpretation(reading_type, cards):
    """إنشاء تفسير للقراءة بناءً على البطاقات"""
    
    interpretations = {
        'single': generate_single_interpretation(cards[0]),
        'past_present_future': generate_ppp_interpretation(cards),
        'relationship': generate_relationship_interpretation(cards),
        'decision': generate_decision_interpretation(cards)
    }
    
    return interpretations.get(reading_type, "")

def generate_single_interpretation(card):
    direction = "معكوسة" if card['reversed'] else "مستقيمة"
    meaning = card['meaning_reversed'] if card['reversed'] else card['meaning_upright']
    
    return f"""
البطاقة: {card['name_ar']} ({direction})

المعنى: {meaning}

الكلمات المفتاحية: {', '.join(card['keywords'])}

التفسير: هذه البطاقة تشير إلى {meaning.split('،')[0]}. في وضعك الحالي، عليك الانتباه إلى هذا الجانب من حياتك.
"""

def generate_ppp_interpretation(cards):
    past_direction = "معكوسة" if cards[0]['reversed'] else "مستقيمة"
    present_direction = "معكوسة" if cards[1]['reversed'] else "مستقيمة"
    future_direction = "معكوسة" if cards[2]['reversed'] else "مستقيمة"
    
    return f"""
الماضي - {cards[0]['name_ar']} ({past_direction}):
{cards[0]['meaning_reversed'] if cards[0]['reversed'] else cards[0]['meaning_upright']}

الحاضر - {cards[1]['name_ar']} ({present_direction}):
{cards[1]['meaning_reversed'] if cards[1]['reversed'] else cards[1]['meaning_upright']}

المستقبل - {cards[2]['name_ar']} ({future_direction}):
{cards[2]['meaning_reversed'] if cards[2]['reversed'] else cards[2]['meaning_upright']}

التفسير الشامل: تُظهر الماضي {cards[0]['keywords'][0]}، بينما الحاضر يعكس {cards[1]['keywords'][0]}، والمستقبل يحمل {cards[2]['keywords'][0]}.
"""

def generate_relationship_interpretation(cards):
    return f"""
أنت: {cards[0]['name_ar']}
{cards[0]['meaning_reversed'] if cards[0]['reversed'] else cards[0]['meaning_upright']}

الطرف الآخر: {cards[1]['name_ar']}
{cards[1]['meaning_reversed'] if cards[1]['reversed'] else cards[1]['meaning_upright']}

العلاقة: {cards[2]['name_ar']}
{cards[2]['meaning_reversed'] if cards[2]['reversed'] else cards[2]['meaning_upright']}

التفسير: العلاقة تحتاج إلى التركيز على {cards[2]['keywords'][0]} و {cards[2]['keywords'][1]}.
"""

def generate_decision_interpretation(cards):
    return f"""
الخيار الأول: {cards[0]['name_ar']}
{cards[0]['meaning_reversed'] if cards[0]['reversed'] else cards[0]['meaning_upright']}

الخيار الثاني: {cards[1]['name_ar']}
{cards[1]['meaning_reversed'] if cards[1]['reversed'] else cards[1]['meaning_upright']}

التوصية: انظر إلى الخيار الذي يتماشى أكثر مع {cards[0]['keywords'][0]} أو {cards[1]['keywords'][0]}.
"""
